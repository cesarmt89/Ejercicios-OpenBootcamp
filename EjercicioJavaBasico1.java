package Ejercicio;

public class EjercicioJavaBasico1 {
    public static void main(String[] args) {

        byte number1 = 10;
        short number2 = 123;
        int number3 = 34413;
        long number4 = 34699331;

        System.out.println("numeros enteros: " + number1 + " " + number2 + " " + number3 + " " + number4);

        float decimal1 = 12.1f;
        double decimal2  = 24.30;

        System.out.println("numeros decimales: " + decimal1 + " " + decimal2);

        char caracter1 = 'a';

        System.out.println("el caracter es: " + caracter1);

        boolean verdadero = true;
        boolean falso = false;

        System.out.println("boolean pueden ser: " + verdadero + " o " + falso);

        String nombre = "Cesar";
        String apellido = "Mattar";

        System.out.println("los el nombre del autor es: " + nombre + " " + apellido);

    }
}       
